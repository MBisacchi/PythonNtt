# Operadores arrtiméticos

# Adição
print (1+1)

# Subtração
print (10-2)

# Multiplicação
print (4*3)

# Divisão quebrada 
print (12/3)

# Divisão inteira
print (12//2)

# Modulo -> resto da divisão
print (10%3)

# Exponenciação -> o numero (x=2) ao cubo ou ao qudrado
print (2**3)

# segue as convenções das regras matemáticas (ex.: 1. (), 2. expoentes, 3. * e /, 4. + e -)
